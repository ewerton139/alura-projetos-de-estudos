const idadeFulano = 30
const minhaIdade = '30'

console.clear()

// Operador igualdade
const comparacaoIgual = idadeFulano == minhaIdade
console.log(`As idades são iguais?: ${comparacaoIgual}`)

// Operador maior que
const coparacaoMaior = idadeFulano > minhaIdade
console.log(`A idade de fulano é maior que a minha?: ${coparacaoMaior}`)


// Operador maior igual que
const coparacaoMaiorIgual = idadeFulano >= minhaIdade
console.log(`A idade de fulano é maior ou igual que a minha?: ${coparacaoMaiorIgual}`)

// Operador menor que
const coparacaoMenor = idadeFulano < minhaIdade
console.log(`A idade de fulano é menor que a minha?: ${coparacaoMenor}`)

// Operador menor igual que
const coparacaoMenorIgual = idadeFulano <= minhaIdade
console.log(`A idade de fulano é menor ou igual que a minha?: ${coparacaoMenorIgual}`)

const idade = 16
