// Comparação estrita (compara se o valor e tipo são iguais)
const comparacaoEstrita = idadeFulano === minhaIdade
console.log(`Fulano é estritamente igual a minha idade?: ${comparacaoEstrita}`)

// Comparação diferente
const comparacaoDiferente = idadeFulano != minhaIdade
console.log(`A idade do fulano é diferente da minha?: ${comparacaoDiferente}`)

// Comparação estrita difente (compara se o valor e tipo são diferente)
const comparacaoEstritaDiferente = idadeFulano !== minhaIdade
console.log(`Fulano é estritamente diferente a minha idade?: ${comparacaoEstritaDiferente}`)




